export const sqmToAcres = (value: number) => {
  return value / 4046.86;
};

export const acresToSqm = (value: number) => {
  return value * 4046.86;
};

export const sqftToSqm = (value: number) => {
  return value / 10.764;
};

export const sqmToSqft = (value: number) => {
  return value * 10.764;
};
